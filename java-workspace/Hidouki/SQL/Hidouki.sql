--データベースの削除
drop database if exists sample;
--データベースの作成
create database if not exists sample;
--データベースの選択
use sample;


--テーブルの削除
drop table if exists user_info;
drop table if exists user_regist_info;
drop table if exists index_master_info;
drop table if exists spending_info;

--登録者テーブルの作成
create table user_info(
id int NOT NULL PRIMARY KEY AUTO_INCREMENT,
user_name varchar(30),
password varchar(30),
deleted varchar(1)
);

--ユーザー登録情報テーブルの作成
create table user_regist_info(
id int NOT NULL PRIMARY KEY AUTO_INCREMENT,
user_id int NOT NULL,
start_regist_date datetime NOT NULL,
last_login datetime NOT NULL,
deleted varchar(1)
);

--項目マスターテーブルの作成
create table index_master_info(
id int NOT NULL PRIMARY KEY AUTO_INCREMENT,
index_number varchar(3),
index_name varchar(255),
index_comment varchar(255),
deleted varchar(1)
);

--収入テーブルの作成
create table income_info(
id int NOT NULL PRIMARY KEY AUTO_INCREMENT,
user_id int,
index_num varchar(3),
income_date datetime,
price int,
deleted varchar(1)
);

--支出テーブルの作成
create table spending_info(
id int NOT NULL PRIMARY KEY AUTO_INCREMENT,
user_id int,
index_num varchar(3),
spending_date datetime,
price int,
deleted varchar(1)
);

